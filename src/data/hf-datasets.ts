/**
 * HuggingFace datasets catalog for Legion Hutta.
 *
 * A static, curated catalog of 100+ popular HuggingFace datasets
 * grouped into thematic categories. Used by the "Datasets" browser
 * in the UI to let users discover and load datasets with one click.
 *
 * All metadata is hand-curated from public HuggingFace dataset cards
 * (downloads are approximate, rounded to the nearest thousand). The
 * `installSnippet` is a ready-to-run Python snippet that loads the
 * dataset with the `datasets` library.
 *
 * No network calls are made — this module is pure data.
 */

export type HfDatasetCategory =
  | "nlp"
  | "vision"
  | "audio"
  | "tabular"
  | "multimodal"
  | "time-series"
  | "reinforcement-learning"
  | "graph"
  | "code"
  | "translation"
  | "dialogue";

export interface HfDataset {
  /** Dataset id on the HF Hub, e.g. "glue" or "Anthropic/hh-rlhf". */
  id: string;
  /** Human-readable name. */
  name: string;
  /** Authoring org or user, e.g. "Google", "Anthropic". */
  author: string;
  category: HfDatasetCategory;
  /** One-paragraph description. */
  description: string;
  /** Approximate download count (last 30 days). */
  downloads: number;
  /** Human-readable size, e.g. "12.4 MB". */
  size?: string;
  /** Notable column / field names. */
  features: string[];
  /** A `from datasets import load_dataset\nds = load_dataset("ID")` snippet. */
  installSnippet: string;
  /** Free-form tags for search. */
  tags: string[];
}

export interface HfDatasetCategoryInfo {
  id: HfDatasetCategory;
  label: string;
  description: string;
  icon: string;
}

