/**
 * Build a `load_dataset` snippet. The optional `config` is appended as
 * the second positional arg — useful for datasets that expose multiple
 * configurations (e.g. "wikitext-2-raw-v1", "en", "de-en").
 */
function snippet(id: string, config?: string): string {
  const cfg = config ? `, "${config}"` : "";
  return `from datasets import load_dataset\nds = load_dataset("${id}"${cfg})`;
}

