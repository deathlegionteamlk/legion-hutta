/**
 * Search datasets by free-text query (matches id, name, description, tags)
 * and optionally filter by category.
 *
 * The match is case-insensitive and splits the query on whitespace so a
 * search like "image classification" matches datasets containing both
 * "image" and "classification" (anywhere in the searchable fields).
 */
export function searchHfDatasets(
  query: string,
  category?: HfDatasetCategory,
): HfDataset[] {
  const q = query.trim().toLowerCase();
  const tokens = q.length > 0 ? q.split(/\s+/) : [];
  return HF_DATASETS.filter((d) => {
    if (category && d.category !== category) return false;
    if (tokens.length === 0) return true;
    const haystack = [
      d.id,
      d.name,
      d.description,
      d.author,
      ...d.tags,
      ...d.features,
    ]
      .join(" ")
      .toLowerCase();
    return tokens.every((t) => haystack.includes(t));
  });
}

/** Return all datasets in a category, sorted by downloads (desc). */
export function getDatasetsByCategory(category: HfDatasetCategory): HfDataset[] {
  return HF_DATASETS.filter((d) => d.category === category).sort(
    (a, b) => b.downloads - a.downloads,
  );
}

/** Look up a single dataset by its hub id. Returns undefined if not found. */
export function getDatasetById(id: string): HfDataset | undefined {
  return HF_DATASETS.find((d) => d.id === id);
}

